SELECT id                   AS Id,
       email                AS Email,
       last_name            AS LastName,
       first_name           AS FirstName,
       middle_name          AS MiddleName,
       phone                AS Phone,
       count_vacation_days  AS CountVacationDays,
       profession_id        AS ProfessionId,
       role_id              AS RoleId,
       division_id          AS DivisionId
FROM account;